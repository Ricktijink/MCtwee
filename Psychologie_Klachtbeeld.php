							Klachtbeelden als symptoom-somatisch vraagstukken, onbegrepen lichamelijke klachten, burnoutachtige dynamiek, trauma’s, depressies, angsten, emotieregulatie-problematiek, seksuele problematiek zijn voorbeelden van DSM-V-vraagstukken die je onze therapeuten voor kunt leggen.

