							Klachtbeelden in deze categorie (wat men noemt “onbegrepen lichamelijk klachten”) hebben meer nodig dan fysiotherapie of psychologie alleen. Een combinatie van fysiek en mentaal aanpakken is nodig om zo’n vicieuze cirkel te doorbreken. Omdat er dan meer nodig is, worden er vaak MDP’s voorgesteld.
