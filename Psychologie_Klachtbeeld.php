							Er zijn een groot aantal klachten/klachtbeelden/symptomen die te maken hebben met de psyche. Sterker nog: wij van MC<sup>2</sup> zijn van mening dat alles rondom gezondheid te maken heeft met de psyche. Of het nu gaat om dat wat je weet over je eigen klachtbeeld, hoe je erover denkt, wat je ermee doet, maar ook hoe de klachten zich uiten bij je, heeft te maken met psychologie. Wanneer je bijvoorbeeld last hebt van je knie is het weliswaar niet zo dat de last blijft bestaan wanneer je deze niet op “denk”-niveau aanpakt, maar het inzetten van je denken heeft wel invloed. Je denken maakt onderdeel uit van je totale gezondheid en maakt jou als persoon uniek met hoe jij omgaat met bepaalde problematiek. De vraagstukken zijn van persoonlijk niveau. Jij gaat ermee om zoals jij dat wil, kunt of mag (soms zelfs moet) van jezelf. Daardoor lopen mensen soms vast in patronen, vicieuze cirkels.
