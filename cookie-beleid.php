      <h2>Wat zijn cookies?</h2>
      <p>
         Cookies zijn kleine tekstbestanden die onze website op je computer, tablet of mobiele telefoon plaatst op het moment dat je de website bezoekt. In deze cookies wordt informatie over jouw websitebezoek opgeslagen. Met het plaatsen van cookies worden persoonsgegevens verwerkt. Door het gebruik van cookies kan een website je herkennen bij een volgend bezoek aan de website. Zo zorgen ze er bijvoorbeeld voor dat alle functionaliteiten en toepassingen van onze website werken en versnellen ze de interactie door bepaalde keuzes die u maakt, te onthouden.
         <br><br>
         Deze cookies hebben verder geen toegang tot jouw computer en kunnen dus geen persoonlijke informatie of andere gegevens van jouw computer lezen. Ze bevatten geen virussen en kunnen geen schadelijke toepassingen op jouw computer installeren.
      </p>

      <h2>Wat voor soorten cookies gebruiken we?</h2>
      <p>
         We maken onderscheid in functionele en niet-functionele cookies. Functionele cookies plaatsen we altijd. Deze zijn noodzakelijk om de website goed te laten werken. Naast functionele cookies plaatsen we ook standaard analytische cookies. Dit is een voorbeeld van een niet-functionele cookie. Hij is namelijk niet nodig voor het goed laten functioneren van de website. Analytische cookies stellen ons in staat om het websitebezoek te meten. Deze statistieken geven ons inzicht in hoe vaak onze website wordt gebruikt, naar welke informatie bezoekers op zoek zijn en welke pagina's het meest worden bezocht. Hierdoor weten we welke website onderdelen populair zijn en op welke plekken we onze website kunnen verbeteren. De statistieken die we verzamelen kunnen we niet herleiden naar personen. Deze gegevens worden dus altijd anoniem gebruikt.
         <br><br>
         Wij verstrekken geen persoonsgegevens aan derden, tenzij dat verplicht is door een wettelijk voorschrift of wanneer dat noodzakelijk is voor het uitvoeren van een overeenkomst die MC2 met je heeft gesloten.
      </p>

      <h2>Hoe kan ik cookies verwijderen en blokkeren?</h2>
      <p>
         Wil je de cookies van onze website niet geplaatst hebben, zorg er dan voor dat je het plaatsen van cookies blokkeert. Realiseer je wel dat als je alle cookies blokkeert, websites minder goed kunnen werken. Het is beter om alleen de ongewenste cookies selectief uit te schakelen. Dit kun je doen in je browser bij de instellingen. Hiermee blokkeer je ook meteen de cookies van andere websites die je bezoekt. Volg de onderstaande link voor het blokkeren van cookies: <a href="https://www.consumentenbond.nl/internet-privacy/cookies-verwijderen#item2" target="_blank">consumentenbond, blokkeren van cookies</a>.
