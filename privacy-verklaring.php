      <h2 id="gegevens">Doorgeven van gegevens</h2>
      <p>
         Wij geven de door jouw verstrekte gegevens door aan verschillende derde partijen, als dit voor het uitvoeren van de overeenkomst noodzakelijk is. Zo geven wij je gegevens bijvoorbeeld door aan je zorgverzekeraar om te kunnen declareren, aan je werkgever om te kunnen factureren en aan de arbodienst om medisch overleg te kunnen plegen. Met bedrijven die je gegevens verwerken in onze opdracht, sluiten wij een verwerkersovereenkomst om te zorgen voor eenzelfde niveau van beveiliging en vertrouwelijkheid van jouw gegevens. Daarin spreken we onder meer af dat gegevens niet voor andere doeleneinden mogen worden gebruikt. Ook komen wij hierin overeen dat jouw gegevens worden verwijderd zodra ze niet meer nodig zijn. MC<sup>2</sup> blijft wel verantwoordelijk voor deze verwerkingen.
         <br><br>
         Verder zullen wij de door jouw verstrekte gegevens niet aan andere partijen verstrekken, tenzij dit wettelijk verplicht of toegestaan is.
      </p>

      <h2 id="lang">Bewaren en beveiligen</h2>
      <p>
         Wij zullen je persoonsgegevens niet langer bewaren dan strikt nodig is om de doelen te kunnen waar maken waarvoor je gegevens zijn verzameld. Uitgezonderd hiervan zijn de gegevens die wij langer moeten bewaren omdat de wet ons dit verplicht, zoals gegevens voor bijvoorbeeld de Belastingdienst. Onze bewaartermijnen zijn:
      </p>
      <ul>
         <li>Cliënten:	einde behandeling + 15 jaar (wettelijke verplichting)</li>
         <li>Opdrachtgevers:	datum verzoek + 4 weken</li>
         <li>Verwijzers:	datum verzoek + 4 weken</li>
         <li>Website bezoekers:	alle gegevens worden na 12 maanden na het bezoek vernietigd</li>
         <li>Sollicitatie gegevens:	tot datum van je afwijzing + 1 maand/ met je goedkeuring 1 jaar / toevoeging aan je personeelsdossier</li>
      </ul>
      <p>
         Wij nemen de bescherming van jouw gegevens serieus en nemen passende maatregelen om misbruik, verlies, onbevoegde toegang, ongewenste openbaarmaking en ongeoorloofde wijziging tegen te gaan. Als jij het idee hebt dat jouw gegevens toch niet goed beveiligd zijn en/of er aanwijzingen zijn van misbruik, bel ons dan onmiddellijk op 013 544 02 42 of mail ons via <a href="mailto:info@mctwee.nl">info@mctwee.nl</a>.
      </p>

      <h2 id="bijzondere">Bijzondere en/of gevoelige persoonsgegevens</h2>
      <p>
         Onze website en/of dienstverlening heeft niet de intentie gegevens te verzamelen over websitebezoekers die jonger zijn dan 16 jaar tenzij ze toestemming hebben van ouders of voogd. We kunnen echter niet controleren of een bezoeker ouder dan 16 is. Wij raden ouders dan ook aan betrokken te zijn bij de online activiteiten van hun kinderen, om zo te voorkomen dat er gegevens over kinderen verzameld worden zonder ouderlijke toestemming. Als je er van overtuigd bent dat wij ondanks alles toch persoonlijke gegevens hebben verzameld over een jeugdige ouder dan 16 jaar, neem dan contact met ons op via <a href="mailto:info@mctwee.nl">info@mctwee.nl</a>, dan zullen wij deze informatie verwijderen.
      </p>

      <h2 id="kaart">Automatische besluitvorming, profilering en cookies</h2>
      <p>
         Via onze website doen wij aan profilering namelijk door het plaatsen van cookies. We doen dat om je de beste online ervaring te kunnen bieden. Deze cookies houden jouw surfgedrag bij zodat we bijvoorbeeld automatisch op maat gemaakte inhoud kunnen aanbieden, of zodat we weten welke pagina’s vaak bezocht worden en welke pagina’s aan verbetering toe zijn. Bij jou eerste bezoek aan onze website hebben wij je geïnformeerd over deze cookies en toestemming gevraagd voor het plaatsen ervan. Wij zullen je op basis hiervan echter nooit voor een van onze diensten en/of producten uitsluiten. Verder doen wij niet aan automatische besluiten. Wil je meer weten over cookies? Lees dan ons <a href="cookie-beleid.php" target="_blank">Cookie beleid</a>.
      </p>

      <h2 id="inzien">Gegevens inzien, aanpassen of verwijderen, toestemming intrekken</h2>
      <p>
         Je hebt het recht om je persoonsgegevens in te zien, te corrigeren en/of te verwijderen. Daarnaast het je ook het recht om je toestemming welke je hebt gegeven voor een bepaalde verwerking van je gegevens in te trekken. Je kunt verzoeken hierover doen via onze contactgegevens (zie <a href="#contactgegevens">Contactgegevens</a>).
